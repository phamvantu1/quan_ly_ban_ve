    @Override
    public void actionPerformed(ActionEvent e) {
        // neu bam search
		JButton btnClicked = (JButton)e.getSource();
		if(btnClicked.equals(btnSearch)){
			if((txtPlace.getText() == null)||(txtPlace.getText().length() == 0))
				return;
			TourDAO rd = new TourDAO();
                        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                        Date dateStart = null;
                        try {
                         dateStart = dateFormat.parse(txtDateStart.getText().trim());
                            } catch (ParseException ex) {
                         ex.printStackTrace();
                    // Xử lý ngoại lệ khi không thể chuyển đổi giá trị sang Date
                        }
                     listTour = rd.searchTour(txtPlace.getText().trim() , dateStart);

                     // hien thi ra bang
                        DefaultTableModel model = (DefaultTableModel) tblResult.getModel();
        model.setRowCount(0); // 

        for (Tour tour : listTour) {
            Object[] row = {
                tour.getID(),
                tour.getName(),
                tour.getPlace(),
                tour.getDateStart(),
                tour.getDateEnd(),
                tour.getPriceTour(),
                tour.getNameService(),
                tour.getNameSupplier(),
                tour.getPriceService(),
                tour.getTotal()
            };
            model.addRow(row);
        }
		}
                else if (e.getSource().equals(btnNext)) // neu bam next
        {
            btnNext1();
        }
        else btnCancel1(); // neu bam  cancel
                
           
                
    }
