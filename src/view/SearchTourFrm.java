    private void tblResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultMouseClicked
        // neu bam chuot
        Tour tour = new Tour();
        int row = tblResult.getSelectedRow();
        tour.setID((int) tblResult.getValueAt(row, 0));
        tour.setName((String) tblResult.getValueAt(row, 1));
        tour.setPlace((String) tblResult.getValueAt(row, 2));
        tour.setDateStart((Date) tblResult.getValueAt(row, 3));
        tour.setDateEnd((Date) tblResult.getValueAt(row, 4));
        tour.setPriceTour((Float) tblResult.getValueAt(row, 5));
        tour.setNameService((String) tblResult.getValueAt(row, 6));
        tour.setNameSupplier((String) tblResult.getValueAt(row, 7));
        tour.setPriceService((Float) tblResult.getValueAt(row, 8));
        tour.setTotal((Float) tblResult.getValueAt(row, 9));
        String numberClient = JOptionPane.showInputDialog("Enter number client:");
        if(numberClient != null && !numberClient.isEmpty()) {
            BookedTour bookedTour = new BookedTour();
            bookedTour.setTour(tour);
            bookedTour.setNumber((int) Float.parseFloat(numberClient));
            booking.getBookedTour().add(bookedTour);
            

        } else {
            JOptionPane.showMessageDialog(this, "Please enter hour visit");
        }
        
        
//        (new SearchClientFrm(user , tour, new Client())).setVisible(true);
//        this.dispose();
        
    }//GEN-LAST:event_tblResultMouseClicked

