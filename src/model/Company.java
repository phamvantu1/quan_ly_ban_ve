package model;

/**
 *
 * @author Admin
 */
public class Company {
    private int id;
	private String name;
	private String address;
	private String note;
	
	public Company() {
		super();
	}

    public Company(int id, String name, String address, String note) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
        
}
